//Size of a int,char,float,double
#include<iostream>
using namespace std;
int main ()
{
     int i;
     char c;
     float f;
     double d;
     cout << "Size of int is: " << sizeof(i)<<" bytes"<<endl;
cout << "Size of char is: " << sizeof(c)<<" bytes"<<endl;
cout << "Size of float is: " << sizeof(f)<<" bytes"<<endl;
cout << "Size of double is: " << sizeof(d)<<" bytes"<<endl;
return 0;
}
